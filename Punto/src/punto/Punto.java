package punto;

public class Punto {  private int x;   private int y;   public Punto() {   x = 0;  y = 0;  }    public Punto(int x, int y) {   this.x = x;  this.y = y;  }  public int obtenX() {   return x;  }  public int obtenY() {   return y;  }  public void cambiaX(int x) {   this.x = x;   }  public void cambiaY(int y) {   this.y = y;   } } 