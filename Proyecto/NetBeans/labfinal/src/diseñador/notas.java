    public void cargarEstudiante(){
        DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
        String sSQL="";
        sSQL="SELECT codigo FROM estudiantes ";
        
        try{
            pst = cn.prepareStatement(sSQL);
            rs = pst.executeQuery(); 
            modeloCombo.addElement(" ");
            while (rs.next()){
  
                modeloCombo.addElement(rs.getObject("codigo"));
            }     
            cboestudiante.setModel(modeloCombo);
        }
        catch (SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
     public void cargarMateria(int cod){

        String sSQL="";
        sSQL="SELECT * FROM materia WHERE codmateria = "+cod;
        
        try{
            pst = cn.prepareStatement(sSQL);
            rs = pst.executeQuery();
            while (rs.next()){
                txtnommateria.setText(rs.getObject("nommateria").toString());
                txtsemestre.setText(rs.getObject("semestre").toString());
                txtcreditos.setText(rs.getObject("numcreditos").toString()); 
            }
        }
        catch (SQLException ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
