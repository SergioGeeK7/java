    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        ConexionMySQL mysql=new  ConexionMySQL();//se llama la clase conexión
        Connection cn=mysql.Conectar();  //creamos un objeto para conectar la base de datos sql

        //ahora creamos las variables  para capturar los valores de los cuadros de texto

        String  codn, code, codcar, codmat, sem, cre, nommat, not;
        String sSQL="";
        String mensaje="";
        codn = txtcodnota.getText();
        code = cboestudiante.getSelectedItem().toString();
        codcar = txtcodcarr.getText();
        codmat = cboMateria.getSelectedItem().toString();
        nommat = txtnommateria.getText();
        sem = txtsemestre.getText();
        cre = txtcreditos.getText();
        not = txtnota.getText();


        sSQL="INSERT INTO notas(codnota, codestudiante, codcarrera, codmateria,"
        + " nommateria, semestre, creditos, notadef)"+
        "VALUES(?, ?, ?, ?, ?, ?, ?, ?)";
        mensaje="los valores se han insertado correctamente";

        //ahora vamos a utilizar una interface con un procedimiento para insertar los datos en la base de datos.

        try
        {
            PreparedStatement pst=cn.prepareStatement(sSQL);
            pst.setString(1,codn);
            pst.setString(2,code);
            pst.setString(3,codcar);
            pst.setString(4,codmat);
            pst.setString(5,nommat);
            pst.setString(6,sem);
            pst.setString(7,cre);
            pst.setString(8,not);

            //ahora este código es para  ver cuántos elementos se insertaron

            int  n=pst.executeUpdate();
            if (n>0){
                JOptionPane.showMessageDialog(null, mensaje);
            }
        }

        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_btnguardarActionPerformed

