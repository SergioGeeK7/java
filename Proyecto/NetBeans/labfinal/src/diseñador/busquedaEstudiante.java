    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if(txtcodest.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Debe selecionar un estudiante");
            btneliminar.setEnabled(false);
            btncancelar.setEnabled(false);
            buscar();
        }
        else{
            ConexionMySQL mysql=new ConexionMySQL();//se llama la clase conexión
            Connection cn=mysql.Conectar(); //creamos un objeto para conectar la base de datos sql
        
            String mts ="DELETE FROM estudiantes WHERE codigo = "+txtcodest.getText();

            try{
                 Statement st = cn.createStatement();//Obtiene procedimiento o consulta, sin parametros.
                 st.execute(mts); //ejecute la consulta, en este caso que elimine.
            }
            catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
                btneliminar.setEnabled(false);
                btncancelar.setEnabled(false);
                txtbuscarcod.setText("");
        txtbuscarnombre.setText("");
        txtcodest.setText("");
        txtnomest.setText("");
        txtapeest.setText("");
        txtdirest.setText("");
        txttelest.setText("");
        txtemaest.setText("");
        txtcelest.setText("");
        txtgenest.setText("");
            buscar();
        }
    }//GEN-LAST:event_btneliminarActionPerformed

