    public ImageIcon cargarFoto(int cod) throws IOException{
        Statement st;
        String SQL = "SELECT foto FROM estudiantes WHERE codigo ="+cod;
        ImageIcon imagen = null;
        InputStream archivo = null;
        try{
            st = cn.createStatement();
            rs = st.executeQuery(SQL);
            if(rs.next()){
                archivo = rs.getBinaryStream(1);
                Image bi = ImageIO.read(archivo).getScaledInstance(lblfoto.getWidth(), lblfoto.getHeight(), 1);
                imagen = new ImageIcon(bi);
            }
        }
        catch(SQLException ex){
            ex.printStackTrace();
        }
        return imagen;
    }
    
